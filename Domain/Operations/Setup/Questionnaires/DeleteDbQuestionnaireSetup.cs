using Common.Interfaces;
using Common.Operations;
using Domain.Entities.Setup;
using Infrastructure.DB;
using Oracle.ManagedDataAccess.Client;
using System;
using System.Collections.Generic;
using System.Data;
using System.Text;
using System.Threading.Tasks;

namespace Domain.Operations.Setup.Questionnaires
{
   public static class DeleteDbQuestionnaireSetup
    {
        public async static Task<IDTO> DeleteQuestionnaireAsync(Questionnaire questionnaire)
        {
            OracleDynamicParameters oracleParams = new OracleDynamicParameters();
            ComplateOperation<int> complate = new ComplateOperation<int>();
            var dyParam = new OracleDynamicParameters();
            dyParam.Add(QuestionnaireSpParams.PARAMETER_ID, OracleDbType.Int64, ParameterDirection.Input, (object)questionnaire.ID ?? DBNull.Value);
            if (await NonQueryExecuter.ExecuteNonQueryAsync(QuestionnaireSpName.SP_DELETE_QUESTIONNAIRE, dyParam) == -1)
                complate.message = "Operation Successed";
            else
                complate.message = "Operation Failed";
            return complate;
        }

        public async static Task<IDTO> DeleteQuestionnairesAsync(long[] IDs)
        {

            ComplateOperation<int> complate = new ComplateOperation<int>();

            if (await NonQueryExecuter.ExecuteNonQueryAsync(MultiDeleteFormater.Format(typeof(Questionnaire), IDs)) == -1)
                complate.message = "Operation Successed";
            else
                complate.message = "Operation Failed";

            return complate;
        }
    }
}
